numeroUm = int (input("escolha um numero: "))
numeroDois = int (input("escolha outro numero: "))

# int foi usado para fazer uma converção de tipo(casting)

soma = numeroUm + numeroDois
print("soma", soma)

subtracao = numeroUm - numeroDois
print("subtracao", subtracao)

divisao = float (numeroUm / numeroDois)
print("divisao", divisao)

divisao2 = (numeroUm // numeroDois)
print("divisao2", divisao2)

multiplicacao = numeroUm * numeroDois
print("multiplicacao", multiplicacao)

potencia = numeroUm ** numeroDois
print("potencia", potencia)

raizQuadrada = numeroUm ** (1/2)
print("raiz quadrada", raizQuadrada)
